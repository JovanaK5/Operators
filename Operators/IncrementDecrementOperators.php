<?php

  // ++
  
  $number1 = 10;
  $number1 += 1;
  echo $number1 . PHP_EOL;

  //post ++
  echo $number1++ . PHP_EOL;
  echo $number1 . PHP_EOL;

  //pre ++
  echo ++$number1 . PHP_EOL;
  echo $number1 . PHP_EOL;

  // --
  
  $number1 = 10;
  $number1 -= 1;
  echo $number1 . PHP_EOL;

  //post --
  echo $number1-- . PHP_EOL;
  echo $number1 . PHP_EOL;

  //pre --
  echo --$number1 . PHP_EOL;
  echo $number1 . PHP_EOL;